function readTsv(path) {
  const lines = readFileSync(path, 'utf8').trimEnd().split('\n');
  return {
    header: lines[0],
    body: lines.slice(1),
  };
}

function wikiUrl(page) {
  return `https://en.wikipedia.org/wiki/${page}`;
}

function commonsSearch(title) {
  return `https://commons.wikimedia.org/wiki/Special:MediaSearch?type=image&search=${encodeURIComponent(title)}`;
}

const { header, body } = readTsv(TSV_PATH);
const existingIds = new Set(body.map((line) => line.split('\t')[1]));
const nextBody = [...body];

for (const card of cards) {
  if (existingIds.has(card.id)) continue;
  nextBody.push([
    nextBody.length + 1,
    card.id,
    card.title,
    card.continent,
    card.epoch,
    card.type,
    'research-pending',
    'needs-row-level-source',
    'priority-omission',
    wikiUrl(card.page),
    commonsSearch(card.title),
    card.note,
  ].join('\t'));
}

writeFileSync(TSV_PATH, `${header}\n${nextBody.join('\n')}\n`);

const details = JSON.parse(readFileSync(DETAILS_PATH, 'utf8'));
for (const card of cards) {
  details[card.id] = {
    year: card.year,
    combatants: card.combatants,
    outcome: card.outcome,
    war: card.war,
    location: card.location,
    series: {
      key: card.series[0],
      label: card.series[1],
      order: card.series[2],
    },
    gloss: card.gloss,
  };
}

writeFileSync(DETAILS_PATH, `${JSON.stringify(details, null, 2)}\n`);

